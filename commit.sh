# Verificar o status do repositório
git status

# Adicionar todos os arquivos ao índice do Git
git add .

# Fazer o commit das mudanças
git commit -m "Adicionar arquivos para o GitHub"

# Verificar o branch atual
git branch

# Enviar as mudanças para o repositório remoto (substitua 'master' pelo seu branch atual)
git push -u origin master

